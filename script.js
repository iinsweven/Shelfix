const searchInput = document.querySelector(".search-input");

if (searchInput) {
    searchInput.addEventListener("keyup", function () {
        let input = searchInput.value.toLowerCase();
        let resultBox = document.getElementById("search-results");
        resultBox.innerHTML = "";
        if (input === "") return;
        let filteredItems = items.filter(item => item.title.toLowerCase().includes(input));
        filteredItems.forEach(item => {
            resultBox.innerHTML += `<div class="result-card" onclick="openDetails('${item.title}')"><img src="${item.image}" class="result-image"><div class="result-info"><h3>${item.title}</h3><p>${item.type} • ${item.year}</p><small>${item.caption}</small></div></div>`;
        });
    });
}

function openDetails(title) {
    window.location.href = "media_details.php?title=" + encodeURIComponent(title);
}

function openHomeDetails(title) {
    window.location.href = "media_details.php?title=" + encodeURIComponent(title);
}



function loadDetails() {

    let selectedTitle =
        localStorage.getItem("selectedItem");

    let item =
        items.find(i => i.title === selectedTitle);

    let container =
        document.getElementById("details-container");

    if (item) {

        container.innerHTML = `

        <div class="details-card">

            <img src="${item.image}"
            class="details-image">

            <h1>${item.title}</h1>

            <h3>${item.type} • ${item.year}</h3>

            <p>${item.caption}</p>

            <div class="rating-section">

           ⭐ ${item.rating}

        </div>

        <div class="review-section">

        <h2>Reviews</h2>

        ${item.reviews.map(review => `

         <p>${review}</p>

         `).join('')}

         </div>

           <div class="button-group">

<button class="review-btn"
onclick="handleRatingAction()">

Add Rating

</button>

<button class="review-btn"
onclick="handleReviewAction()">

Add Review

</button>

</div>

        </div>

        `;
    }
    window.scrollTo({

        top: 0,

        behavior: "smooth"
    });
}

